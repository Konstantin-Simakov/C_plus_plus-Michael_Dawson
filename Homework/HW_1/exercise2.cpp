#include <iostream>

int main(void)
{
	using namespace std;
	// Integer dividing
	cout << "Seven divided by three is " << 7 / 3 << endl;
	// Float dividing
	cout << "Seven divided by three is " << 7.0 / 3 << endl;
	// Float dividing too
	cout << "Seven divided by three is " << 7 / 3.0 << endl;
	// Float dividing too
	cout << "Seven divided by three is " << 7.0 / 3.0 << endl;

	return 0;
}
