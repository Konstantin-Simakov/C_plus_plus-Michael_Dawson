// expensive_calculator.cpp 
// Demonstrates working with embedded arithmetic operations.
#include <iostream>

int main(void)
{
	using namespace std;
	cout << "8 + 3 = " << 8 + 3 << endl;
	cout << "8 - 3 = " << 8 - 3 << endl;
	cout << "8 * 3 = " << 8 * 3 << endl;
	cout << "8 / 3 = " << 8 / 3 << endl;
	cout << "8.0 / 3.0 = " << 8.0 / 3.0 << endl;
	cout << "8 % 3 = " << 8 % 3 << endl;
	cout << "8 + 3 * 5 = " << 8 + 3 * 5 << endl;
	cout << "(8 + 3) * 5 = " << (8 + 3) * 5 << endl;

	return 0;
}
